#include "Operator.hpp"

 int Operator::addAction(int so1, int so2) {
    return so1 + so2;
}

int Operator::subAction(int so1, int so2) {
    return  so1 - so2;
}

int Operator::mulAction(int so1, int so2) {
    return  so1 * so2;
}

int Operator::divAction(int so1, int so2) {
    return  so1 / so2;
}
